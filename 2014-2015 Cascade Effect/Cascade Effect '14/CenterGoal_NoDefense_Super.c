task servoInit() {
	while(true)
		servo[dump] = 66;
}

task main()
{
	initializeRobot();
	StartTask(getHeading);
	int irPosition = getPosition();
	servo[dump] = 66;
	waitForStart();
	servo[dump] = 66;

	if(irPosition == 2)//horizontal
	{
		playSound(soundUpwardTones);
		coastTo(2);
		wait1Msec(200);
		rotateCoast(12,-1);
		coastTo(75);//moving towards goal
		wait1Msec(500);
		rotateCoast(90,1);
		wait1Msec(500);
		coastTo(15); //approaching goal
		wait1Msec(500);
		coastTo(-3);
		wait1Msec(1000);
		raiseLift();
		advBrakeNeg();
		wait1Msec(100);
		dumpAll();
		lowerLift();
	}

	else if(irPosition == 1)//45 deg
	{
		playSound(soundDownwardTones);
		coastTo(5);
		wait1Msec(200);
		rotateCoast(10,-1);
		wait1Msec(200);
		coastTo(40.5);
		rotateCoast(45, 1);
		coastTo(17);//hit center goal
		coastTo(-3); //back up
		wait1Msec(200);
		raiseLift();
		advBrakeNeg();
		dumpAll();
		lowerLift();
	}

	else if(irPosition == 0)//vertical
	{
		coastTo(5);
		rotateCoast(30,1);
		//
		coastRaw(1250);
		//
		wait1Msec(200);
		rotateCoast(36,-1);
		wait1Msec(200);
		coastTo(30);
		coastTo(-3);
		wait1Msec(500);
		raiseLift();
		advBrakeNeg();
		dumpAll();
		lowerLift();
	}

	//Kickstand code
	rotateCoast(80,1);
	wait1Msec(200);
	coastTo(20);
	rotateCoast(90,-1);
	wait1Msec(200);
	coastTo(60);//running into kickstand
	//turn on collector
	rotateCoast(90,1);//rotate for collection
	startCollector();
	coastTo(-40);//move to collect/defense
	stopCollector();

	while(true)
		wait1Msec(1000);

	playSound(soundUpwardTones);
	wait1Msec(5000000);

}
